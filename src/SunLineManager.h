#ifndef	__SUNLINEMANAGER_H__
#define __SUNLINEMANAGER_H__

//////////////////////////////////////////////////
// 不完全型宣言
//////////////////////////////////////////////////
class CSunLine;

//////////////////////////////////////////////////
// 太陽線マネージャー
//////////////////////////////////////////////////
class CSunLineManager
{
public:	// 外部公開

	//////////////////////////////////////////////////
	// メソッド
	//////////////////////////////////////////////////

	// コンストラクタ
	CSunLineManager();

	// デストラクタ
	~CSunLineManager();

	//////////////////////////////////////////////////
	//	・関数名	Update
	//	・説明		更新を行う
	//	・引数		なし
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Update();

	//////////////////////////////////////////////////
	//	・関数名	Draw
	//	・説明		描画を行う
	//				加算合成で板ポリゴンを描画
	//	・引数		
	//		- lpdevice	デバイス
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Draw( LPDIRECT3DDEVICE9 lpdevice ) const;

	//////////////////////////////////////////////////
	//	・関数名	SetRandom
	//	・説明		指定された数分ランダムに太陽線を出す
	//				ゲームメインクラスの海範囲を上限とする
	//	・引数		
	//		- num	出す本数
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void SetRandom( UINT num );

private:	// 非公開

	//////////////////////////////////////////////////
	// 型宣言
	//////////////////////////////////////////////////
	typedef std::list<CSunLine*>	SUNLINELIST;	// リスト型

	//////////////////////////////////////////////////
	// フィールド
	//////////////////////////////////////////////////
	SUNLINELIST		m_SunLineList;		// 太陽線リスト
};

#endif	// __SUNLINEMANAGER_H__