#ifndef __AKI_SCREW_H__
#define __AKI_SCREW_H__

////////////////////////////////////
// スクリュークラス
////////////////////////////////////
class CScrew
{
public:	// 外部公開

	////////////////////////////////////
	// メソッド
	////////////////////////////////////

	// コンストラクタ
	CScrew();

	// デストラクタ
	~CScrew();

	//////////////////////////////////////////////////
	//	・関数名	Update
	//	・説明		親子関係方式で更新を行う
	//	・引数		
	//		- mat	親の行列
	//		- speed	親の速さ(尻尾の動く速さに影響する)
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Update( const D3DXMATRIX& mat, float speed );

	//////////////////////////////////////////////////
	//	・関数名	Draw
	//	・説明		描画を行う
	//	・引数		
	//		- lp_device	デバイス
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Draw( const LPDIRECT3DDEVICE9& lpdevice );

	//////////////////////////////////////////////////
	//	・関数名	GetMatrix
	//	・説明		行列を獲得する
	//	・引数		なし
	//	・戻り値	行列を返す
	//////////////////////////////////////////////////
	inline D3DXMATRIX GetMatrix() const
	{
		return m_mMat;
	}

private:	// 非公開

	////////////////////////////////////
	// フィールド
	////////////////////////////////////
	CXfile*	m_lpMesh;	// スクリューのメッシュ
	CMatrix	m_mMat;		// 行列(ローカル前提)
};

#endif // __AKI_SCREW_H__