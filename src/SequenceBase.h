#ifndef __AKI_SEQUENCEBASE_H__
#define __AKI_SEQUENCEBASE_H__

namespace Sequence
{
	//////////////////////////////////////////////////
	// シーケンスインターフェイス
	//////////////////////////////////////////////////
	class IBase
	{
	public:	// 外部公開

		// 仮想デストラクタ
		virtual ~IBase(){}

		//////////////////////////////////////////////////
		//	・関数名	RunProc
		//	・説明		マネージャーで常に呼ばれる関数
		//				派生先では、処理本体となる
		//	・引数		なし	
		//	・戻り値	まだ更新を行うならtrue
		//				破棄するならばfalseを返す
		//////////////////////////////////////////////////
		virtual bool RunProc() = 0;

		//////////////////////////////////////////////////
		//	・関数名	GetNext
		//	・説明		次のシーケンスを獲得する
		//				派生先では、次のシーケンスを返すようにする
		//	・引数		なし	
		//	・戻り値	次のシーケンスインターフェイスを
		//				ポインタで返す
		//				NULLを返せば終了する
		//////////////////////////////////////////////////
		virtual IBase* GetNext() = 0;

		//////////////////////////////////////////////////
		//	・関数名	FileLoad
		//	・説明		読み込みを行う
		//				派生先では、外部読み込み類を実行させる
		//	・引数		なし	
		//	・戻り値	なし
		//////////////////////////////////////////////////
		virtual void FileLoad() = 0;
	};
}

#endif // __AKI_SEQUENCEBASE_H__