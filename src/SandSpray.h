#ifndef	__AKI_SANDSPRAY_H__
#define __AKI_SANDSPRAY_H__

//////////////////////////////////////////////////
// 砂しぶきクラス
// ポイントスプライトで表現
// ポイントスプライト自体は外部で設定して渡してもらう
//////////////////////////////////////////////////
class CSandSpray
{
public:	// 外部公開

	/////////////////////////////////
	// メソッド
	/////////////////////////////////

	// コンストラクタ
	CSandSpray();

	// デストラクタ
	~CSandSpray();

	//////////////////////////////////////////////////
	//	・関数名	Update
	//	・説明		更新を行う
	//	・引数		なし
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Update();

	//////////////////////////////////////////////////
	//	・関数名	Draw
	//	・説明		描画を行う
	//	・引数		
	//		- lp_device デバイス
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Draw( LPDIRECT3DDEVICE9 lp_device );

	//////////////////////////////////////////////////
	//	・関数名	Add
	//	・説明		砂煙の追加
	//				削除も行うので動的確保で渡すこと
	//	・引数		
	//		- lp_sand 内容を設定したポイントスプライト
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void Add( CPointSpriteManager* lp_sand );

	//////////////////////////////////////////////////
	//	・関数名	AddDefault
	//	・説明		こちらで設定した砂煙を追加
	//	・引数		
	//		- pos	出現させる位置
	//	・戻り値	なし
	//////////////////////////////////////////////////
	void AddDefault( const D3DXVECTOR3& pos );

private:	// 非公開

	/////////////////////////////////
	// 型宣言
	/////////////////////////////////
	typedef std::list<CPointSpriteManager*>	LISTTYPE;

	/////////////////////////////////
	// フィールド
	/////////////////////////////////
	LISTTYPE	m_SandList;	// 砂リスト	
};

#endif // __AKI_SANDSPRAY_H__